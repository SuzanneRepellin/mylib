#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int k;
	int nb;

	i = 0;
	k = 1;
	nb = 0;
	while (str[i] != '\0' && (str[i] == ' ' || str[i] == '\t' ||
			str[i] == '\v' || str[i] == '\n' || str[i] == '\r' ||
			str[i] == '\f'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			k = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * k);
}
