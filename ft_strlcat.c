#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	char		*d;
	const char	*s;
	size_t		n;
	size_t		l;

	n = size;
	s = src;
	d = dst;
	while (n-- != 0 && *d != '\0')
		d++;
	l = d - dst;
	n = size - l;
	if (n == 0)
		return (l + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (l + (s - src));
}
