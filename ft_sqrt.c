#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;

	if (nb < 0)
		return (0);
	a = 1;
	while (a * a != nb)
	{
		if (a > 46341)
			return (0);
		a++;
	}
	if (nb % a != 0)
		a = 0;
	return (a);
}
