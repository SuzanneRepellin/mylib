#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*u;

	i = 0;
	u = (unsigned char *)s;
	if (n)
	{
		while (i < n)
		{
			u[i] = 0;
			i++;
		}
	}
}
